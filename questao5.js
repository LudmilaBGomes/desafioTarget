let string = 'LudmilaBrito123';
let stringReversa = ''

for (let i = string.length - 1; i >= 0; --i) {
    stringReversa += string[i];
}

console.log(`A string reversa de ${string} é ${stringReversa}`);

/*
=========== Saída ===========

A string reversa de LudmilaBrito123 é 321otirBalimduL

==============================
*/